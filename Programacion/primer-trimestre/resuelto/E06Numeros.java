
public class E06Numeros {

	public static void main(String[] args) {
		// DECLARACION DE VARIABLES
		int cont;
		
		//Mostrar 10 veces en pantalla "hola" con bucle ascendente.
		cont=0;
		while(cont<10 ) {
			System.out.println("Hola");
			cont=cont+1;
			
		}
		
		//Mostrar 10 veces en pantalla "Adios" con bucle descendente
		cont=10;
		while(cont>0) {
			System.out.println("Adios");
			cont=cont-1;
		}
	}

}
